    /**
     * Returns true when this manager should produce rings for the given NAIF body ID.
     *
     * @param naifId NAIF integer code of the body being processed
     * @return true only for Saturn (NAIF 699)
     */
    static boolean appliesToBody(int naifId) {
        return naifId == KepplrConstants.SATURN_NAIF_ID;
    }

    /**
     * Returns Saturn's ring plane normal in J2000 space given the J2000-to-body-fixed rotation.
     *
     * <p>The ring plane is the equatorial (XY) plane in body-fixed space, so its normal in body-fixed space is Z = (0,
     * 0, 1). Applying the transpose (body-fixed → J2000) of the J2000-to-body-fixed rotation matrix R gives the third
     * row of R: {R[2][0], R[2][1], R[2][2]}.
     *
     * @param rot J2000-to-body-fixed rotation matrix from KEPPLREphemeris (may be null)
     * @return unit normal in J2000 space, or null if rot is null
     */
    static double[] extractRingPlaneNormal(RotationMatrixIJK rot) {
        if (rot == null) return null;
        return new double[] {rot.get(2, 0), rot.get(2, 1), rot.get(2, 2)};
    }

    /**
     * Update ring geometry for the current frame.
     *
     * <p>If saturnBsn is null (Saturn culled or rendered as sprite), the ring node is detached. Otherwise the ring is
     * attached to Saturn's bodyFixedNode and per-frame uniforms are updated.
     *
     * @param saturnBsn Saturn's BodySceneNode when DRAW_FULL; null otherwise
     * @param cameraHelioJ2000 camera heliocentric J2000 position in km, double[3]
     */
    void update(BodySceneNode saturnBsn, double[] cameraHelioJ2000) {
        if (saturnBsn == null) {
            detach();
            return;
        }

        // Ensure ring geometry is built
        if (ringNode == null) {
            buildRingGeometry();
        }

        // Attach to bodyFixedNode if not already attached
        Node targetFixedNode = saturnBsn.bodyFixedNode;
        if (attachedTo != targetFixedNode) {
            if (attachedTo != null) {
                attachedTo.detachChild(ringNode);
            }
            targetFixedNode.attachChild(ringNode);
            attachedTo = targetFixedNode;
        }

        updateUniforms(saturnBsn, cameraHelioJ2000);
    }

    /** Detach the ring node from the scene graph. */
    void detach() {
        if (ringNode != null && attachedTo != null) {
            attachedTo.detachChild(ringNode);
            attachedTo = null;
        }
    }

    // ── private helpers ──────────────────────────────────────────────────────────────────────────

    private void buildRingGeometry() {
        ringGeom = new Geometry(SUN_HALO_OCCLUDER_NODE_NAME, buildAnnulusMesh());
        ringGeom.setUserData("shadowRole", "CASTER|RECEIVER|SUN_HALO_OCCLUDER");
        ringGeom.setQueueBucket(com.jme3.renderer.queue.RenderQueue.Bucket.Transparent);

        ringMaterial = buildRingMaterial();
        ringGeom.setMaterial(ringMaterial);

        ringNode = new Node(SUN_HALO_OCCLUDER_NODE_NAME + "-node");
        ringNode.attachChild(ringGeom);
    }

    /**
     * Build the ring annulus mesh in body-fixed km.
     *
     * <p>Vertices lie in the XY plane (Z=0). Radial positions are in km matching the shader uniforms InnerRadius and
     * OuterRadius, so no position scaling is needed. No UV coordinates are emitted — the fragment shader computes
     * radial UV from inPosition.xy.
     */
    private static Mesh buildAnnulusMesh() {
        int segments = KepplrConstants.SATURN_RING_ANGULAR_SEGMENTS;
        float inner = (float) KepplrConstants.SATURN_RING_INNER_RADIUS_KM;
        float outer = (float) KepplrConstants.SATURN_RING_OUTER_RADIUS_KM;

        int vertexCount = (segments + 1) * 2;
        FloatBuffer positions = BufferUtils.createFloatBuffer(vertexCount * 3);
        FloatBuffer normals = BufferUtils.createFloatBuffer(vertexCount * 3);
        IntBuffer indices = BufferUtils.createIntBuffer(segments * 6);

        for (int i = 0; i <= segments; i++) {
            double angle = 2.0 * Math.PI * i / (double) segments;
            float cos = (float) Math.cos(angle);
            float sin = (float) Math.sin(angle);

            // inner vertex
            positions.put(inner * cos).put(inner * sin).put(0f);
            normals.put(0f).put(0f).put(1f);

            // outer vertex
            positions.put(outer * cos).put(outer * sin).put(0f);
            normals.put(0f).put(0f).put(1f);
        }
        positions.flip();
        normals.flip();

        for (int i = 0; i < segments; i++) {
            int base = i * 2;
            int inner0 = base;
            int outer0 = base + 1;
            int inner1 = base + 2;
            int outer1 = base + 3;
            indices.put(inner0).put(outer0).put(outer1);
            indices.put(inner0).put(outer1).put(inner1);
        }
        indices.flip();

        Mesh mesh = new Mesh();
        mesh.setBuffer(VertexBuffer.Type.Position, 3, positions);
        mesh.setBuffer(VertexBuffer.Type.Normal, 3, normals);
        mesh.setBuffer(VertexBuffer.Type.Index, 3, indices);
        mesh.updateBound();
        return mesh;
    }

    private Material buildRingMaterial() {
        Material mat = new Material(assetManager, "kepplr/shaders/Rings/SaturnRings.j3md");
        mat.setTexture("BrightnessTex", buildLuminanceTexture(RING_BRIGHTNESS));
        mat.setTexture("TransparencyTex", buildLuminanceTexture(RING_TRANSPARENCY));
        mat.setColor(
                "RingColor",
                new ColorRGBA(
                        KepplrConstants.SATURN_RING_COLOR_R,
                        KepplrConstants.SATURN_RING_COLOR_G,
                        KepplrConstants.SATURN_RING_COLOR_B,
                        1f));
        mat.setFloat("InnerRadius", (float) KepplrConstants.SATURN_RING_INNER_RADIUS_KM);
        mat.setFloat("OuterRadius", (float) KepplrConstants.SATURN_RING_OUTER_RADIUS_KM);

        // Placeholder values — overwritten every frame by updateUniforms()
        mat.setVector3("SunDir", new Vector3f(1f, 0f, 0f));
        mat.setVector3("ObserverPos", Vector3f.ZERO);

        // Step 16b shadow parameters — disabled in 16a (defaults from .j3md are 0)
        // ShadowDarkness, MoonShadowDarkness, NumShadowCasters stay at default 0

        var rs = mat.getAdditionalRenderState();
        rs.setBlendMode(BlendMode.Alpha);
        rs.setDepthWrite(false);
        rs.setDepthTest(true);
        rs.setFaceCullMode(FaceCullMode.Off);
        return mat;
    }

    /**
     * Update per-frame shader uniforms.
     *
     * <p>SunDir and ObserverPos are expressed in body-fixed (model) space by rotating the J2000 vectors using the
     * inverse of bodyFixedNode's world rotation. This avoids passing a separate world-to-local matrix uniform.
     */
    private void updateUniforms(BodySceneNode saturnBsn, double[] cameraHelioJ2000) {
        // Saturn's camera-relative scene position (J2000/world space)
        Vector3f saturnScene = saturnBsn.ephemerisNode.getWorldTranslation();

        // Sun scene position = heliocentric origin in scene space
        // Camera is at scene origin; sun (at heliocentric 0,0,0) is at −cameraHelioJ2000.
        float sunX = (float) -cameraHelioJ2000[0];
        float sunY = (float) -cameraHelioJ2000[1];
        float sunZ = (float) -cameraHelioJ2000[2];

        // Sun direction from Saturn in J2000/world space (normalized)
        float dsx = sunX - saturnScene.x;
        float dsy = sunY - saturnScene.y;
        float dsz = sunZ - saturnScene.z;
        float sunLen = (float) Math.sqrt(dsx * dsx + dsy * dsy + dsz * dsz);
        Vector3f sunDirJ2000 =
                (sunLen > 0f) ? new Vector3f(dsx / sunLen, dsy / sunLen, dsz / sunLen) : new Vector3f(1f, 0f, 0f);

        // Observer (camera) position relative to Saturn in J2000/world space
        // Camera is at scene origin (0,0,0); Saturn is at saturnScene.
        Vector3f obsRelJ2000 = new Vector3f(-saturnScene.x, -saturnScene.y, -saturnScene.z);

        // Rotate both into body-fixed space via inverse of bodyFixedNode world rotation
        Quaternion worldToBodyFixed = saturnBsn.bodyFixedNode.getWorldRotation().inverse();
        Vector3f sunDirBodyFixed = worldToBodyFixed.mult(sunDirJ2000);
        Vector3f obsBodyFixed = worldToBodyFixed.mult(obsRelJ2000);

        ringMaterial.setVector3("SunDir", sunDirBodyFixed);
        ringMaterial.setVector3("ObserverPos", obsBodyFixed);
    }

    /**
     * Bake a double[] profile into a 1-D Luminance8 texture.
     *
     * <p>Algorithm derived from prototype: SaturnRingsController — reimplemented for new architecture.
     */
    private static Texture2D buildLuminanceTexture(double[] values) {
        int size = values.length;
        ByteBuffer buf = BufferUtils.createByteBuffer(size);
        for (double v : values) {
            double clamped = Math.max(0.0, Math.min(1.0, v));
            buf.put((byte) ((int) Math.round(clamped * 255.0) & 0xFF));
        }
        buf.flip();

        Image image = new Image(Image.Format.Luminance8, size, 1, buf, com.jme3.texture.image.ColorSpace.Linear);
        Texture2D tex = new Texture2D(image);
        tex.setMinFilter(Texture.MinFilter.Trilinear);
        tex.setMagFilter(Texture.MagFilter.Bilinear);
        tex.setAnisotropicFilter(4);
        tex.setWrap(Texture.WrapMode.Clamp);
        return tex;
    }
